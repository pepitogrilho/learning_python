# import <module_name>   .......................................
import math
print(math.pi)
#                        .......................................
#                        ..............................
#.............................................

#.............................................
# import type 02                   .....................
# import <module_name> as <alias>  .............................
import math as m
print(m.pi)
#                        .......................................
#                        ..............................
#.............................................

#.............................................
# import type 03                     ...................
# from <module_name> import <part>   ...........................
from math import pi, sqrt
print(math.pi)
print(math.sqrt(16))
#                        .......................................
#                        ..............................
#.............................................

#.............................................
# import type 04                     ...................
# from <module_name> import <part> as <alias>  .................
from math import sqrt as raiz
print(raiz(16))
